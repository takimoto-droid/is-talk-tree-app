export function getBuiltinUCDefault(id: string): CustomUseCase {
  return {
    id,
    title: BUILTIN_UC_DEFAULT_TITLES[id] ?? id,
    prompt: BUILTIN_UC_DEFAULT_PROMPTS[id] ?? '',
    fields: [],
    createdAt: new Date().toISOString(),
  };
}
